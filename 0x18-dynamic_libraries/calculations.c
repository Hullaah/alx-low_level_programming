/**
 * add - adds two numbers together.
 * @a: number
 * @b: number
 * Return: sum of numbers
*/
int add(int a, int b)
{
        return (a + b);
}
/**
 * sub - subtracts two numbers from each other
 * @a: number
 * @b: number
 * Return: difference of numbers
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two numbers together.
 * @a: number
 * @b: number
 * Return: product of numbers
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides a by b
 * @a: number
 * @b: number
 * Return: division of numbers
*/
int divide(int a, int b)
{
	return (a / b);
}
/**
 * mod - gets the remainder of a when divided by b.
 * @a: number
 * @b: number
 * Return: modulus of numbers
*/
int mod(int a, int b)
{
	return (a % b);
}
